let currentLang = 'bn';

function toggleLang() {
    currentLang = currentLang === 'bn' ? 'en' : 'bn';
    updateContent();
}

function updateContent() {
    const t = translations[currentLang];
    document.getElementById('langText').textContent = t.lang;
    document.getElementById('mainTitle').textContent = t.mainTitle;
    document.querySelector("title").innerText = t.mainTitle;

    const navElements = document.querySelectorAll('[id^="nav"]');
    navElements.forEach(elem => {
        const key = elem.id;
        if (t[key]) {
            const textNode = Array.from(elem.childNodes).find(node => node.nodeType === 3);
            if (textNode) {
                textNode.textContent = t[key];
            } else {
                elem.childNodes[0].textContent = t[key];
            }
        }
    });

    // Update all submenu items
    const allSubmenuItems = document.querySelectorAll('.submenu-item');
    allSubmenuItems.forEach(elem => {
        const key = elem.id;
        if (t[key]) {
            elem.textContent = t[key];
        }
    });

    // Update footer content
    document.getElementById('footerTitle').textContent = t.footerTitle;
    document.getElementById('footerEiin').textContent = t.footerEiin;
    document.getElementById('footerAddress').textContent = t.footerAddress;
    document.getElementById('footerContact').textContent = t.footerContact;
    document.getElementById('footerEmail').textContent = t.footerEmail;
    document.getElementById('footerDevLabel').textContent = t.footerDevLabel;

    // Update notice content
    document.getElementById('noticeTitle').textContent = t.noticeTitle;
    document.getElementById('viewAllBtn').textContent = t.viewAllBtn;
    document.getElementById('notice1').textContent = t.notice1;
    document.getElementById('notice2').textContent = t.notice2;
    document.getElementById('notice3').textContent = t.notice3;
    document.getElementById('notice4').textContent = t.notice4;
    document.getElementById('notice5').textContent = t.notice5;
    document.getElementById('noticeSlideText').textContent = t.noticeSlideText;
    document.getElementById('founderHeader').textContent = t.founderHeader;
    document.getElementById('founderName').textContent = t.founderName;
    document.getElementById('founderTitle').textContent = t.founderTitle;

    // Update principal section
    document.getElementById('principalHeader').textContent = t.principalHeader;
    document.getElementById('principalName').textContent = t.principalName;
    document.getElementById('principalTitle').textContent = t.principalTitle;

    // Update super section
    document.getElementById('superHeader').textContent = t.superHeader;
    document.getElementById('superName').textContent = t.superName;
    document.getElementById('superTitle').textContent = t.superTitle;

    // Update links section
    document.getElementById('linksHeader').textContent = t.linksHeader;
    for (let i = 1; i <= 11; i++) {
        const linkElem = document.getElementById('link' + i);
        if (linkElem) {
            const linkTextElem = linkElem.querySelector('.link-text');
            if (linkTextElem) {
                linkTextElem.textContent = t['link' + i];
            }
        }
    }

    document.getElementById('jubilee-header').textContent = t.jubilee;
    document.getElementById('campus-header').textContent = t.campus;
    document.getElementById('admission-header').textContent = t.admission;
    document.getElementById('academic-header').textContent = t.academic;
    document.getElementById('paper-header').textContent = t.paper;
    document.getElementById('teacher-header').textContent = t.teacher;
    document.getElementById('result-header').textContent = t.result;
    document.getElementById('resources-header').textContent = t.resources;
    document.getElementById('gallery-header').textContent = t.gallery;

    // Update all list items
    const sections = ['jubilee', 'campus', 'admission', 'academic', 'paper', 'teacher', 'result', 'resources', 'gallery'];
    sections.forEach(section => {
        const items = t[section + 'Items'];
        items.forEach((text, i) => {
            const elem = document.getElementById(section + '-item' + (i + 1));
            if (elem) elem.textContent = text;
        });
    });
}

